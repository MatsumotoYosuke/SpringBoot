package form;

public class RegisterForm {
	
	
	private String customerName;
	private String customerNameKana;
	private String customerNo;
	private String postalCode;
	private String streetAddress;
	private String streetAddressKana;
	
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomerNameKana() {
		return customerNameKana;
	}
	public void setCustomerNameKana(String customerNameKana) {
		this.customerNameKana = customerNameKana;
	}
	public String getCustomerNo() {
		return customerNo;
	}
	public void setCustomerNo(String customerNo) {
		this.customerNo = customerNo;
	}
	public String getPostalCode() {
		return postalCode;
	}
	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}
	public String getStreetAddress() {
		return streetAddress;
	}
	public void setStreetAddress(String streetAddress) {
		this.streetAddress = streetAddress;
	}
	public String getStreetAddressKana() {
		return streetAddressKana;
	}
	public void setStreetAddressKana(String streetAddressKana) {
		this.streetAddressKana = streetAddressKana;
	}
}
